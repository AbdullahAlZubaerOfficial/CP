#include <stdio.h>   // ইনপুট ও আউটপুট ব্যবস্থার জন্য স্ট্যান্ডার্ড লাইব্রেরি

int main() { 
   int a, b;         // দুটি পূর্ণসংখ্যা ইনপুটের জন্য
   char s;           // একটি চিহ্ন ইনপুটের জন্য (>, <, =)

   scanf("%d %c %d", &a, &s, &b);   // ইনপুট নিচ্ছে: সংখ্যা-চিহ্ন-সংখ্যা

   // ✅ সঠিক সম্পর্ক যাচাই করছি
   if ((s == '>' && a > b) || (s == '<' && a < b) || (s == '=' && a == b)) {
      printf("Right");   // যদি সম্পর্ক মিলে যায়, তাহলে "Right"
   } else {
      printf("Wrong");   // নাহলে "Wrong"
   }

   return 0;   // প্রোগ্রাম শেষ
}


/*
📥 Input:
5 > 3

📤 Output:
Right

📥 Input:
4 = 7

📤 Output:
Wrong

📝 Short Note:
- এই প্রোগ্রামটি ব্যবহারকারীর দেওয়া দুটি সংখ্যা এবং একটি তুলনামূলক চিহ্ন (>, <, =) নিয়ে যাচাই করে সেটা **সত্য** কিনা।
- যদি চিহ্ন অনুযায়ী সংখ্যাগুলোর সম্পর্ক সত্য হয়, তাহলে "Right" প্রিন্ট করবে, নাহলে "Wrong"।
- টাইম কমপ্লেক্সিটি: O(1) — কারণ এটা কেবল একটি চেক করে।
*/

