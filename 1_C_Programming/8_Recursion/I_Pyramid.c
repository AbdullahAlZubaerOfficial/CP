#include <stdio.h>  // ЁЯМЯ Standard I/O header

// space() тЖТ ржирж┐рж░рзНржжрж┐рж╖рзНржЯ рж╕ржВржЦрзНржпржХ space ржкрзНрж░рж┐ржирзНржЯ ржХрж░рзЗ (recursive)
void space(int count) {
    if (count == 0) {    // тЫФ base-case: ржЖрж░ space рж▓рж╛ржЧржмрзЗ ржирж╛
        return;
    }
    printf(" ");         // 1ржЯрж╛ space
    space(count - 1);    // ЁЯФБ ржмрж╛ржХрж┐ space-ржПрж░ ржЬржирзНржп recursive call
}

// star() тЖТ ржирж┐рж░рзНржжрж┐рж╖рзНржЯ рж╕ржВржЦрзНржпржХ * ржкрзНрж░рж┐ржирзНржЯ ржХрж░рзЗ (recursive)
void star(int count) {
    if (count == 0) {    // тЫФ base-case: ржЖрж░ star рж▓рж╛ржЧржмрзЗ ржирж╛
        return;
    }
    printf("*");         // 1ржЯрж╛ *
    star(count - 1);     // ЁЯФБ ржмрж╛ржХрж┐ star-ржПрж░ ржЬржирзНржп recursive call
}

// outerLoop() тЖТ ржкрзНрж░рждрж┐ржЯрж┐ рж▓рж╛ржЗржи ржмрж╛ржирж╛рзЯ: space + star, рждрж╛рж░ржкрж░ newline
void outerLoop(int i, int n) {
    if (i > n) {         // тЫФ base-case: рж╕ржм рж▓рж╛ржЗржи рж╣рзЯрзЗ ржЧрзЗрж▓рзЗ ржерж╛ржоржмрзЗ
        return;
    }

    space(n - i);        // ЁЯФ╣ leading spaces
    star(2 * i - 1);     // ЁЯФ╕ ржорж╛ржЭрзЗрж░ stars   (ржкрж┐рж░рж╛ржорж┐ржб ржЖрж▓рж╛ржжрж╛ рж╣рзЯ)
    printf("\n");        // тЦ╢я╕П ржкрж░рзЗрж░ рж▓рж╛ржЗржирзЗ ржпрж╛ржУрзЯрж╛

    outerLoop(i + 1, n); // ЁЯФБ ржкрж░ржмрж░рзНрждрзА рж▓рж╛ржЗржи
}

int main() {
    int n;               // ЁЯП╖я╕П ржХрзЯржЯрж╛ рж▓рж╛ржЗржи рж╣ржмрзЗ
    scanf("%d", &n);     // ЁЯУе ржЗржиржкрзБржЯ

    outerLoop(1, n);     // ЁЯЪА ржкрж┐рж░рж╛ржорж┐ржб рж╢рзБрж░рзБ (i = 1)

    return 0;            // тЬЕ ржкрзНрж░рзЛржЧрзНрж░рж╛ржо рж╢рзЗрж╖
}

