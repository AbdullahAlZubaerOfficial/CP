/*------------- ЁЯФв star() тЖТ 1 ржерзЗржХрзЗ count ржкрж░рзНржпржирзНржд рж╕ржВржЦрзНржпрж╛ ржкрзНрж░рж┐ржирзНржЯ -------------*/
void star(int j, int count){    
    if(j > count){          // тЫФ base-case: j limit ржЫрж╛ржбрж╝рж╛рж▓рзЗ ржерж╛ржоржмрзЗ
        return;
    }
    printf("%d", j);        // ЁЯФв ржмрж░рзНрждржорж╛ржи рж╕ржВржЦрзНржпрж╛ ржкрзНрж░рж┐ржирзНржЯ
    star(j + 1, count);     // ЁЯФБ ржкрж░ржмрж░рзНрждрзА рж╕ржВржЦрзНржпрж╛рж░ ржЬржирзНржп recursive call
}

/*------------- тмЬ space() тЖТ ржирж┐рж░рзНржжрж┐рж╖рзНржЯ рж╕ржВржЦрзНржпржХ space ржкрзНрж░рж┐ржирзНржЯ -------------*/
void space(int count){
    if(count == 0){         // тЫФ base-case: ржЖрж░ space рж▓рж╛ржЧржмрзЗ ржирж╛
        return;
    }
    printf(" ");            // тмЬ ржПржХржЯрж┐ space
    space(count - 1);       // ЁЯФБ ржмрж╛ржХрж┐ space
}

/*------------- ЁЯУд outerLoop() тЖТ ржкрзНрж░рждрж┐ржЯрж┐ рж▓рж╛ржЗржи ржЖржБржХрзЗ -------------*/
void outerLoop(int i, int n){
    if(i > n){              // тЫФ рж╕ржм рж▓рж╛ржЗржи рж╣ржпрж╝рзЗ ржЧрзЗрж▓рзЗ ржерж╛ржоржмрзЗ
        return;
    }
    space(n - i);           // тмЕя╕П leading spaces (ржбрж╛ржиржжрж┐ржХрзЗ рж╕рж░рж╛рждрзЗ)
    star(1, i);             // тЮбя╕П 1 тАж i ржкрж░рзНржпржирзНржд рж╕ржВржЦрзНржпрж╛
    printf("\n");           // тЦ╢я╕П newline
    outerLoop(i + 1, n);    // ЁЯФБ ржкрж░рзЗрж░ рж▓рж╛ржЗржи (i тЖЧ)
}

int main(){
    int n;                  // ЁЯП╖я╕П ржорзЛржЯ рж▓рж╛ржЗржи
    scanf("%d", &n);        // ЁЯУе ржЗржиржкрзБржЯ
    outerLoop(1, n);        // ЁЯЪА ржкрзНржпрж╛ржЯрж╛рж░рзНржи ржЖржБржХрж╛ рж╢рзБрж░рзБ
    return 0;               // тЬЕ ржкрзНрж░рзЛржЧрзНрж░рж╛ржо рж╕ржорж╛ржкрзНржд
}

