    if (i < a) {               // ‡¶Ø‡¶¶‡¶ø index ar1 ‡¶è‡¶∞ range ‡¶è‡¶∞ ‡¶Æ‡¶ß‡ßç‡¶Ø‡ßá ‡¶π‡ßü
        d[i] = ar1[i];         // ‡¶§‡¶æ‡¶π‡¶≤‡ßá ar1 ‡¶•‡ßá‡¶ï‡ßá d[] ‡¶§‡ßá ‡¶ï‡¶™‡¶ø ‡¶ï‡¶∞‡ßã
    } else if (i < a + b) {    // ‡¶Ø‡¶¶‡¶ø index ar2 ‡¶è‡¶∞ ‡¶Æ‡¶ß‡ßç‡¶Ø‡ßá ‡¶π‡ßü (ar1 ‡¶è‡¶∞ ‡¶™‡¶∞‡ßá)
        d[i] = ar2[i - a];     // ar2 ‡¶•‡ßá‡¶ï‡ßá d[] ‡¶§‡ßá ‡¶ï‡¶™‡¶ø ‡¶ï‡¶∞‡ßã, offset adjust ‡¶ï‡¶∞‡ßá
    } else {                   // ‡¶®‡ßü‡¶§ ar3 ‡¶è‡¶∞ index ‡¶π‡¶¨‡ßá
        d[i] = ar3[i - a - b]; // ar3 ‡¶•‡ßá‡¶ï‡ßá d[] ‡¶§‡ßá ‡¶ï‡¶™‡¶ø ‡¶ï‡¶∞‡ßã, offset adjust ‡¶ï‡¶∞‡ßá
    }

