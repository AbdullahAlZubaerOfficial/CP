#include <stdio.h>
#include <string.h>

int main()
{
    int n;                      // টেস্ট কেস এর সংখ্যা ইনপুট নেওয়া হবে
    scanf("%d", &n);

    while (n--)
    {
        char s[10001];          // প্রতিটি ইনপুট স্ট্রিং রাখা হবে এখানে

        scanf("%s", s);         // স্ট্রিং ইনপুট নেওয়া

        int capital = 0, small = 0, digits = 0;  // ক্যাপিটাল, স্মল এবং ডিজিট কাউন্টার

        // স্ট্রিং এর প্রতিটি ক্যারেক্টার চেক করা
        for (int i = 0; s[i] != '\0'; i++)
        {
            if (s[i] >= 'a' && s[i] <= 'z')      // ছোট হাতের অক্ষর হলে
            {
                small++;
            }
            else if (s[i] >= 'A' && s[i] <= 'Z') // বড় হাতের অক্ষর হলে
            {
                capital++;
            }
            else if (s[i] >= '0' && s[i] <= '9') // ডিজিট হলে
            {
                digits++;
            }
        }

        // ক্যাপিটাল, স্মল, এবং ডিজিট সংখ্যা প্রিন্ট করা
        printf("%d %d %d\n", capital, small, digits);
    }

    return 0;
}

