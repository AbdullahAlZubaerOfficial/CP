#include <stdio.h>
#include <string.h> // strlen() ফাংশনের জন্য প্রয়োজনীয় লাইব্রেরি

int main()
{
    char s1[101];
    char s2[101];

    scanf("%s", s1); // প্রথম স্ট্রিং ইনপুট
    scanf("%s", s2); // দ্বিতীয় স্ট্রিং ইনপুট

    int length1 = strlen(s1); // s1 এর দৈর্ঘ্য
    int length2 = strlen(s2); // s2 এর দৈর্ঘ্য

    printf("%d %d\n", length1, length2); // উভয় স্ট্রিংয়ের দৈর্ঘ্য প্রিন্ট
    printf("%s%s\n", s1, s2); // দুইটি স্ট্রিং একসাথে প্রিন্ট

    // দুইটি স্ট্রিংয়ের প্রথম অক্ষর অদলবদল
    char temp = s1[0];
    s1[0] = s2[0];
    s2[0] = temp;

    printf("%s %s", s1, s2); // পরিবর্তনের পর স্ট্রিং দুটি প্রিন্ট

    return 0;
}

