#include <stdio.h>  // ইনপুট-আউটপুট ফাংশনের জন্য হেডার ফাইল

int main()
{
    char s[100001];                  // ইনপুট স্ট্রিং রাখার জন্য অ্যারে
    scanf("%s", s);                  // স্ট্রিং ইনপুট নিচ্ছে (স্পেস ছাড়া)

    int count = 0;                   // কনসোন্যান্টের সংখ্যা রাখার জন্য কাউন্টার

    for (int i = 0; s[i] != '\0'; i++)  // স্ট্রিংয়ের শেষ পর্যন্ত ঘুরছে
    {
        // চেক করছে ছোট হাতের কনসোন্যান্ট কিনা
        if (s[i] > 'a' && s[i] <= 'z' && s[i] != 'e' && s[i] != 'i' && s[i] != 'o' && s[i] != 'u')
        {
            count++;                 // কনসোন্যান্ট পেলে কাউন্ট বাড়াচ্ছে
        }
    }

    printf("%d", count);            // কনসোন্যান্ট সংখ্যা প্রিন্ট করছে

    return 0;                       // প্রোগ্রাম সফলভাবে শেষ 😊
}

