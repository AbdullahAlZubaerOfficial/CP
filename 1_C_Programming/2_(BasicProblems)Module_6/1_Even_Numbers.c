#include <stdio.h>              // ইনপুট-আউটপুট ফাংশনের জন্য স্ট্যান্ডার্ড হেডার

int main() {                    // প্রোগ্রামের মূল ফাংশন শুরু
    int n;                      // n ভ্যারিয়েবল ঘোষণা (সীমা সংখ্যাটি ধরবে)

    scanf("%d", &n);            // ইউজারের কাছ থেকে n ইনপুট নিচ্ছি

    for (int i = 1; i <= n; i++) { // ১ থেকে n পর্যন্ত লুপ চালাই
        if (i % 2 == 0) {           // যদি i জোড় হয় (২ দিয়ে ভাগশেষ ০)
            printf("%d\n", i);      // তাহলে সেই জোড় সংখ্যা প্রিন্ট করি
        }
    }

    return 0;                   // প্রোগ্রাম সফলভাবে শেষ
}

