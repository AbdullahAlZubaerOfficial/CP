#include <stdio.h> // ইনপুট-আউটপুট ব্যবহারের জন্য হেডার ফাইল

int main() {       // মেইন ফাংশন শুরু
    int n;         // একটি ইন্টিজার ভ্যারিয়েবল n ডিক্লেয়ার

    while (scanf("%d", &n)) // যতক্ষণ ইউজার সংখ্যা ইনপুট দিবে ততক্ষণ লুপ চলবে
    {
        if(n == 1999){             // যদি ইনপুট 1999 হয়
            printf("Correct\n");   // তাহলে "Correct" প্রিন্ট করবে
            break;                 // এবং লুপ থামাবে
        }
        else{                      // অন্য যেকোনো সংখ্যা ইনপুট দিলে
            printf("Wrong\n");     // "Wrong" প্রিন্ট করবে
        }
    }

    return 0; // প্রোগ্রাম সফলভাবে শেষ
}

/*
Input:
1111  
1234  
1999

Output:
Wrong  
Wrong  
Correct

Note: ইউজার যতক্ষণ না 1999 ইনপুট দেয়, ততক্ষণ "Wrong" দেখাবে। যখন 1999 ইনপুট দিবে তখন "Correct" প্রিন্ট করে লুপ থেমে যাবে।
*/
