#include <stdio.h>      // ইনপুট ও আউটপুটের জন্য হেডার ফাইল
#include <limits.h>     // INT_MIN ব্যবহারের জন্য এই হেডার

int main() { 
   int n, x;            // n = কতটি সংখ্যা ইনপুট নেবে, x = প্রতিটি ইনপুটের জন্য ব্যবহার হবে
   scanf("%d", &n);     // ইউজার থেকে সংখ্যা n ইনপুট নেওয়া

   int mx = INT_MIN;    // সর্বোচ্চ মান স্টোর করার জন্য mx, শুরুতে খুবই ছোট মান দিয়ে initialize

   for(int i = 0; i < n; i++){  // n বার লুপ চলবে
       scanf("%d", &x);         // প্রতিবার একটি করে সংখ্যা ইনপুট নেবে
       if(x > mx){             // যদি ইনপুট করা সংখ্যা বর্তমান mx থেকে বড় হয়
           mx = x;             // তাহলে mx আপডেট করবে
       }
   }

   printf("%d", mx);           // সব থেকে বড় সংখ্যাটি প্রিন্ট করবে

   return 0; // প্রোগ্রাম সফলভাবে শেষ
}

/*
Input:
5
12 45 67 23 89

Output:
89

Note: ইউজার যতগুলো সংখ্যা দেবে (উদাহরণে ৫টি), তার মধ্যে সর্বোচ্চ সংখ্যাটি খুঁজে বের করে প্রিন্ট করবে।
*/
