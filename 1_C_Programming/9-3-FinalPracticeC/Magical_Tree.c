#include <stdio.h>

int main() {
    int n;
    scanf("%d", &n);                         // ইউজার থেকে n ইনপুট নেয়া

    int total_lines = n / 2 + 6;             // ট্রায়াঙ্গেলের মোট লাইন সংখ্যা হিসাব (n এর উপর ভিত্তি করে)

    int max_width = 2 * total_lines - 1;    // সবচেয়ে বড় লাইনটির দৈর্ঘ্য (স্টার সংখ্যা)

    // উপরের ট্রায়াঙ্গেল অংশ প্রিন্ট করা
    for (int i = 1; i <= total_lines; i++) {
        int stars = 2 * i - 1;               // প্রতিটি লাইনে স্টার সংখ্যা (অদ্ভুত সংখ্যা)
        int spaces = (max_width - stars) / 2; // স্টারগুলোকে কেন্দ্রিকরণের জন্য স্পেসের সংখ্যা

        for (int j = 0; j < spaces; j++) {  // স্পেস প্রিন্ট করা
            printf(" ");
        }
        for (int j = 0; j < stars; j++) {   // স্টার প্রিন্ট করা
            printf("*");
        }
        printf("\n");                       // নতুন লাইনে যাওয়া
    }

    // নিচের ৫ লাইন উচ্চতার রেক্ট্যাঙ্গেল অংশ প্রিন্ট করা
    for (int i = 0; i < 5; i++) {
        int spaces = (max_width - n) / 2;   // রেক্ট্যাঙ্গেলকে কেন্দ্রিকরণের জন্য স্পেস

        for (int j = 0; j < spaces; j++) {  // স্পেস প্রিন্ট করা
            printf(" ");
        }
        for (int j = 0; j < n; j++) {       // n স্টার প্রিন্ট করা
            printf("*");
        }
        printf("\n");                       // নতুন লাইনে যাওয়া
    }

    return 0;                              // প্রোগ্রাম শেষ
}

