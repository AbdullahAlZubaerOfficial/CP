#include <stdio.h>

int main() { 
    int s, n;

    scanf("%d", &n);          // একটি সংখ্যা ইনপুট নিচ্ছি

    s = n / 1000;             // সংখ্যাটির প্রথম ১-৪ সংখ্যার থেকে হাজারের ঘরের মান নিচ্ছি

    if (s % 2 == 0) {
        // যদি হাজারের ঘরের মান জোড় হয়
        printf("EVEN");
    } else {
        // না হলে বিজোড়
        printf("ODD");
    }

    return 0;
}

/*
ইনপুট:
8273

আউটপুট:
ODD
*/
