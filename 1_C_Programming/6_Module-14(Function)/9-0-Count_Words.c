#include <stdio.h>  // 📚 ইনপুট–আউটপুট লাইব্রেরি

int main() { 
    char s[1000001];                 // 🔸 ১,০০,০০০ অক্ষর পর্যন্ত স্ট্রিং ধরে রাখবে
    
    fgets(s, 1000001, stdin);        // 🔸 সম্পূর্ণ লাইন ইনপুট নেয় (স্পেসসহ)

    int count = 0;                   // 🔸 মোট শব্দের সংখ্যা গুনতে কাউন্টার
    int word  = 0;                   // 🔸 “ভিতরে কি এখন শব্দের মাঝখানে আছি?” বোঝাতে ফ্ল্যাগ

    for (int i = 0; s[i] != '\0'; i++) {          // 🔸 স্ট্রিংয়ের শেষ পর্যন্ত ঘুরছি
        if ((s[i] >= 'a' && s[i] <= 'z') ||       // 🔹 যদি ছোট হাতের অক্ষর হয়
            (s[i] >= 'A' && s[i] <= 'Z')) {       // 🔹 অথবা বড় হাতের অক্ষর হয়
            if (!word) {                          // 🌟 নতুন শব্দ শুরু হলে
                count++;                          //    শব্দ কাউন্ট বাড়াও
                word = 1;                         //    এখন আমরা শব্দের ভিতর
            }
        } else {                                  // 🔸 অক্ষর না হলে (স্পেস, পাংকচুয়েশন ইত্যাদি)
            word = 0;                             //    “শব্দের বাইরে” ফ্ল্যাগ সেট
        }
    }

    printf("%d", count);          // 🔸 মোট শব্দের সংখ্যা প্রিন্ট

    return 0;                     // 🏁 প্রোগ্রাম সফলভাবে শেষ
}


/*
🔹 Input:
This   is  a test.

🔹 Output:
4

🔍 Short Note:
ইনপুট লাইনে (স্পেসসহ) যতগুলো শব্দ আছে, সেগুলো গুনে প্রিন্ট করে।  
একটানা A–Z বা a–z অক্ষরের সিকোয়েন্সকে ‘শব্দ’ ধরা হয়েছে; অন্য যে কোনো ক্যারেক্টার (স্পেস, কমা, ডট) শব্দ বিভাজক হিসেবে ধরা হয়েছে।

🕒 Time Complexity (TC): O(n)  
  ▸ পুরো স্ট্রিং একবার স্ক্যান করা হয়, n = অক্ষরসংখ্যা।

💾 Space Complexity (SC): O(1)  
  ▸ স্থায়ীভাবে শুধু কয়েকটি ইন্টিজার ভ্যারিয়েবল ব্যবহার করা হয়েছে—কোনো অতিরিক্ত অ্যারে/ডাটা স্ট্রাকচার নয়।
*/
