#include <stdio.h>  // Standard input-output লাইব্রেরি

int main() { 
   int r,c;  // মেট্রিক্স এর রো ও কলাম সংখ্যা
   scanf("%d %d",&r,&c);  // ইউজার থেকে রো এবং কলাম ইনপুট নেওয়া
   
   int ar[r][c];  // ২D অ্যারে তৈরি (মেট্রিক্স)

   for(int i=0; i<r; i++){  // প্রতিটি রো এর জন্য
    for(int j=0; j<c; j++){  // প্রতিটি কলাম এর জন্য
        scanf("%d",&ar[i][j]);  // মেট্রিক্স এর উপাদান ইনপুট নেওয়া
    }
   }

   int sum = 0;  // ডায়াগোনালের উপাদানগুলোর যোগফল রাখার ভেরিয়েবল
 

   for(int i=0; i<r; i++){  // ডায়াগোনাল এলিমেন্ট গুলো যোগ করবো
    sum = sum + ar[i][i];  // মূল ডায়াগোনাল উপাদান যোগ করা
   }

    int diagonalize = ar[0][0];  // প্রথম ডায়াগোনাল উপাদান ধরে রাখা

    int isUnit = 1;  // চেক করার জন্য ফ্ল্যাগ, ধরে নিচ্ছি এটি ইউনিট ম্যাট্রিক্স

    if(r==c){  // মেট্রিক্স স্কোয়ার কিনা চেক
        for(int i=0; i<r; i++){
            for(int j=0; j<c; j++){
                if((i!=j) && (ar[i][j]!=0)){  // ডায়াগোনালের বাইরে সব এলিমেন্ট 0 কিনা চেক
                    isUnit = 0;  // যদি না হয় তাহলে ইউনিট ম্যাট্রিক্স না
                    break;
                }
                if((i==j) && (ar[i][j] != diagonalize)){  // ডায়াগোনাল এলিমেন্টগুলো একই কিনা চেক
                    isUnit = 0;  // যদি না হয় তাহলে ইউনিট ম্যাট্রিক্স না
                    break;
                }
            }
        }
    }else{
        isUnit = 0;  // যদি স্কোয়ার না হয় তাহলে ইউনিট ম্যাট্রিক্স না
    }


    if(isUnit && sum == 3){  // যদি সব শর্ত মিলে এবং ডায়াগোনালের যোগফল 3 হয় (3x3 ইউনিট ম্যাট্রিক্স)
        printf("This is Unit Matrix");
    }
    else{
        printf("This is not Unit Matrix");
    }

//    printf("%d",sum);  // sum = 3 (টেস্টিং এর জন্য প্রিন্ট করা যেতো)
   
    return 0;
}

