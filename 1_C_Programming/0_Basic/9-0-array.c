#include <stdio.h>

/*
 * ৫টি উপাদান নিয়ে int অ্যারে বানাই
 * ও সবগুলো ছাপাই।
 */
int main() {
    // একটি int টাইপের অ্যারে ডিক্লেয়ার করলাম এবং ইনিশিয়াল মান দিয়ে দিলাম
    int numbers[5] = {1, 2, 3, 4, 5};

    // for লুপ ব্যবহার করে প্রতিটি উপাদান প্রিন্ট করবো
    for (int i = 0; i < 5; i++) {
        printf("%d ", numbers[i]); // প্রতিটি index থেকে মান বের করে প্রিন্ট
    }

    return 0; // প্রোগ্রাম সফলভাবে শেষ
}

/*
Expected Output:
    1 2 3 4 5 
*/
