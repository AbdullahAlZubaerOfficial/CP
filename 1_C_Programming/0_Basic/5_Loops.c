#include <stdio.h>

/* 1 থেকে 5 পর্যন্ত সংখ্যা প্রিন্ট করি—for লুপ দিয়ে */
int main() {
   // for লুপ শুরু: i = 1 থেকে শুরু, i <= 5 পর্যন্ত, প্রতি ধাপে i++
    for (int i = 1; i <= 5; i++) {
        printf("%d ", i); // i এর মান প্রিন্ট করলাম
    }

    return 0; // প্রোগ্রাম সফলভাবে শেষ
}

/*
Expected Output:
    1 2 3 4 5 
*/
